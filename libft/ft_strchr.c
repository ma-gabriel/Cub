#include <stdlib.h>

char	*ft_strchr(char *str, int c)
{
	size_t	i;

	i = -1;
	while (str[++i])
		if (str[i] == (char) c)
			return (str + i);
	if (!((char) c))
		return (str + i);
	return (0);
}
