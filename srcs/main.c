#include <stdbool.h>
#include <fcntl.h>
#include "libft.h"
#include "map_checks.h"
#include "struct.h"

static void	strs_free(char **strs)
{
	size_t	i;

	i = 0;
	while (strs[i])
	{
		free(strs[i]);
		i++;
	}
	free(strs);
}

int	main(int argc, char **argv)
{
	t_thegame	game;

	ft_bzero(&game, sizeof(game));
	if (!check_arg(argc, argv[1]))
		return (1);
	struct_init(&game, argv[1]);
	if (game.map)
		strs_free(game.map);
	return (0);
}
